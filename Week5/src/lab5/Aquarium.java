package lab5;

public class Aquarium {
	private int numberFish;
	
	//constructor
	public Aquarium(int numberFish) {
		this.numberFish = numberFish;
	}

	public int getNumberFish() {
		return numberFish;
	}

	public boolean addFish() {
		this.numberFish++;
		return true;
	}
	public boolean removeFish(int number) {
		if (numberFish>=number) {
			numberFish-=number;
			return true;
		}
		return false;
	}
	public boolean removeFish() {
		if (numberFish!=0) {
			numberFish--;
			return true;
		}
		return false;
	}
	
	
	
}
